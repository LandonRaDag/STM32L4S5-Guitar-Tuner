	//welcome message
	sprintf(msg, "Hold blue button to begin in ear tuner, hold again for mic. Tap to switch strings\r\n");
	HAL_UART_Transmit(&huart1, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);

